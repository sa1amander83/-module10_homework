var array = [2,3,4,61,2,1,432,'dcsad',40,-20,0,null];
var odd = 0, even = 0, zero = 0;
for(let i = 0; i < array.length; i++) {
	if (typeof array[i] === 'number' && !isNaN(array[i])) {
		if (array[i] === 0) {
			zero++;
		} else {
			array[i] % 2 === 0? even++ : odd++;
		}
	}
}
console.log(`Четных элементов в массиве - ${even}, нечетных элементов в массиве - ${odd}, нулевых элементов в массиве - ${zero}`);

