// 2. Когда вы хотите отобрать только числа, проверки на typeof недостаточно, нужно добавлять и проверку на NaN, т.к. тип значения NaN тоже равен number.
// 3. Из-за того, что в последнем условии проверка не строгая (== вместо ===), к нулям добавляются и другие значения, которые при нестрогом сравнении приравниваются к нулю (например, undefined и false). Т.к. нам нужны только числа-нули, проверка должна быть строгой

